export const CATEGORIES: Category[] = ['Politician', 'Hedge Fund', 'AI', 'Index', 'Community']

/** The tokenized universe available to vault builders. */
export function tokenizedUniverse(): Holding[] {
  const seen = new Map<string, Holding>()
  for (const p of PILOTS)
    for (const h of p.holdings)
      if (h.ticker !== 'CASH' && h.ticker !== 'SHORT' && !seen.has(h.ticker)) seen.set(h.ticker, h)
  return [...seen.values()].sort((a, b) => a.ticker.localeCompare(b.ticker))
}
